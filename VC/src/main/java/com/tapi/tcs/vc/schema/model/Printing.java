package com.tapi.tcs.vc.schema.model;

import java.io.Serializable;

import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.SequenceGenerator;
import javax.persistence.Table;

/**
 * 印刷厂模型
 * 
 * @author Lincoln
 * 
 */
@Entity
@Table(name = "Printing")
@SequenceGenerator(name = "SEQ_PRINTING", sequenceName = "SEQ_PRINTING", allocationSize = 1)
public class Printing implements Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = -4258291443433083001L;

	/** 印刷厂标识 */
	@Id
	@GeneratedValue(strategy = GenerationType.SEQUENCE, generator = "SEQ_PRINTING")
	private Long id;

	/** 印刷厂代码 */
	private String printingCode;

	/** 印刷厂名称 */
	private String printingName;

	/** 工商代码 */
	private String taxNo;

	/** 联系人 */
	private String contact;

	/** 地址 */
	private String address;

	/** 联系电话 */
	private String tel;

	/** 传真 */
	private String fax;

	/** 电子邮件 */
	private String email;

	/** 开户银行代码 */
	private String bankCode;

	/** 开户银行名称 */
	private String bankName;

	/** 账户号 */
	private String bankAccountNo;

	/** 账户名称 */
	private String bankAccountName;

	/**
	 * @return the id
	 */
	public Long getId() {
		return id;
	}

	/**
	 * @param id
	 *            the id to set
	 */
	public void setId(Long id) {
		this.id = id;
	}

	/**
	 * @return the printingCode
	 */
	public String getPrintingCode() {
		return printingCode;
	}

	/**
	 * @param printingCode
	 *            the printingCode to set
	 */
	public void setPrintingCode(String printingCode) {
		this.printingCode = printingCode;
	}

	/**
	 * @return the printingName
	 */
	public String getPrintingName() {
		return printingName;
	}

	/**
	 * @param printingName
	 *            the printingName to set
	 */
	public void setPrintingName(String printingName) {
		this.printingName = printingName;
	}

	/**
	 * @return the taxNo
	 */
	public String getTaxNo() {
		return taxNo;
	}

	/**
	 * @param taxNo
	 *            the taxNo to set
	 */
	public void setTaxNo(String taxNo) {
		this.taxNo = taxNo;
	}

	/**
	 * @return the contact
	 */
	public String getContact() {
		return contact;
	}

	/**
	 * @param contact
	 *            the contact to set
	 */
	public void setContact(String contact) {
		this.contact = contact;
	}

	/**
	 * @return the address
	 */
	public String getAddress() {
		return address;
	}

	/**
	 * @param address
	 *            the address to set
	 */
	public void setAddress(String address) {
		this.address = address;
	}

	/**
	 * @return the tel
	 */
	public String getTel() {
		return tel;
	}

	/**
	 * @param tel
	 *            the tel to set
	 */
	public void setTel(String tel) {
		this.tel = tel;
	}

	/**
	 * @return the fax
	 */
	public String getFax() {
		return fax;
	}

	/**
	 * @param fax
	 *            the fax to set
	 */
	public void setFax(String fax) {
		this.fax = fax;
	}

	/**
	 * @return the email
	 */
	public String getEmail() {
		return email;
	}

	/**
	 * @param email
	 *            the email to set
	 */
	public void setEmail(String email) {
		this.email = email;
	}

	/**
	 * @return the bankCode
	 */
	public String getBankCode() {
		return bankCode;
	}

	/**
	 * @param bankCode
	 *            the bankCode to set
	 */
	public void setBankCode(String bankCode) {
		this.bankCode = bankCode;
	}

	/**
	 * @return the bankName
	 */
	public String getBankName() {
		return bankName;
	}

	/**
	 * @param bankName
	 *            the bankName to set
	 */
	public void setBankName(String bankName) {
		this.bankName = bankName;
	}

	/**
	 * @return the bankAccountNo
	 */
	public String getBankAccountNo() {
		return bankAccountNo;
	}

	/**
	 * @param bankAccountNo
	 *            the bankAccountNo to set
	 */
	public void setBankAccountNo(String bankAccountNo) {
		this.bankAccountNo = bankAccountNo;
	}

	/**
	 * @return the bankAccountName
	 */
	public String getBankAccountName() {
		return bankAccountName;
	}

	/**
	 * @param bankAccountName
	 *            the bankAccountName to set
	 */
	public void setBankAccountName(String bankAccountName) {
		this.bankAccountName = bankAccountName;
	}

}
