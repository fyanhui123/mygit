package com.tapi.tcs.vc.webservice.provider.ClassInfoService.bean;

import javax.xml.bind.annotation.XmlElement;


public class ClassInfoProductFactoryRequestBody {
	/**
	 * 险类代码
	 */
	private String classCode;
	/**
	 * 险类中文全称
	 */
	private String className;
	/**
	 * 险类中文简称
	 */
	private String classNameSimple;
	/**
	 * 险类英文全称
	 */
	private String classEName;
	/**
	 * 险类英文简称
	 */
	private String classENameSimple;
	/**
	 * 险类繁体全称
	 */
	private String classTName;
	/**
	 * 险类繁体简称
	 */
	private String classTNameSimple;
	/**
	 * 是否启用（1：是、0：否）
	 */
	private String validind;
	/**
	 * 备注
	 */
	private String remark;
	public String getClassCode() {
		return classCode;
	}
	@XmlElement(name = "classCode", required = true)
	public void setClassCode(String classCode) {
		this.classCode = classCode;
	}
	public String getClassName() {
		return className;
	}
	@XmlElement(name = "className", required = true)
	public void setClassName(String className) {
		this.className = className;
	}
	public String getClassNameSimple() {
		return classNameSimple;
	}
	@XmlElement(name = "classNameSimple", required = true)
	public void setClassNameSimple(String classNameSimple) {
		this.classNameSimple = classNameSimple;
	}
	public String getClassEName() {
		return classEName;
	}
	@XmlElement(name = "classEName", required = true)
	public void setClassEName(String classEName) {
		this.classEName = classEName;
	}
	
	public String getClassENameSimple() {
		return classENameSimple;
	}
	@XmlElement(name = "classENameSimple", required = true)
	public void setClassENameSimple(String classENameSimple) {
		this.classENameSimple = classENameSimple;
	}
	public String getClassTName() {
		return classTName;
	}
	@XmlElement(name = "classTName", required = true)
	public void setClassTName(String classTName) {
		this.classTName = classTName;
	}
	public String getClassTNameSimple() {
		return classTNameSimple;
	}
	@XmlElement(name = "classTNameSimple", required = true)
	public void setClassTNameSimple(String classTNameSimple) {
		this.classTNameSimple = classTNameSimple;
	}
	public String getValidind() {
		return validind;
	}
	@XmlElement(name = "validind", required = true)
	public void setValidind(String validind) {
		this.validind = validind;
	}
	public String getRemark() {
		return remark;
	}
	@XmlElement(name = "remark", required = true)
	public void setRemark(String remark) {
		this.remark = remark;
	}
}
