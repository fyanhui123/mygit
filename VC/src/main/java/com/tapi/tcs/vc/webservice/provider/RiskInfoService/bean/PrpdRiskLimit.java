package com.tapi.tcs.vc.webservice.provider.RiskInfoService.bean;

import javax.xml.bind.annotation.XmlElement;
/**
 * 险种限额免赔信息
 */
public class PrpdRiskLimit {
	/**
	 * 限额\免赔代码
	 */
	private String limitCode;
	/**
	 * 限额\免赔中文名称
	 */
	private String limitName;
	/**
	 * 限额\免赔繁体名称
	 */
	private String limitTName;
	/**
	 * 限额\免赔英文名称
	 */
	private String limitEName;
	/**
	 * 限额\免赔分类：（0：限额、1：免赔、2：给付比例、3：观察期）
	 */
	private String limitState;
	/**
	 * 免赔单位（1金额、2天数、3次数、4月数、5周、6申报限额、7百分比、8人数、9个数、10小时）
	 */
	private String limitType;
	/**
	 * 核心险别
	 */
	private String kindCode;
	/**
	 * 核心险别中文全称
	 */
	private String kindName;
	/**
	 * 币别
	 */
	private String currency;
	/**
	 * 数值
	 */
	private String value;
	/**
	 * 是否打印在保单上
	 */
	private String isPrint;
	/**
	 * 状态
	 */
	private String state;
	/**
	 * 备注
	 */
	private String remark;
	public String getLimitCode() {
		return limitCode;
	}
	@XmlElement(name = "limitCode", required = false)
	public void setLimitCode(String limitCode) {
		this.limitCode = limitCode;
	}
	public String getLimitName() {
		return limitName;
	}
	@XmlElement(name = "limitName", required = false)
	public void setLimitName(String limitName) {
		this.limitName = limitName;
	}
	public String getLimitTName() {
		return limitTName;
	}
	@XmlElement(name = "limitTName", required = false)
	public void setLimitTName(String limitTName) {
		this.limitTName = limitTName;
	}
	public String getLimitEName() {
		return limitEName;
	}
	@XmlElement(name = "limitEName", required = false)
	public void setLimitEName(String limitEName) {
		this.limitEName = limitEName;
	}
	public String getLimitState() {
		return limitState;
	}
	@XmlElement(name = "limitState", required = false)
	public void setLimitState(String limitState) {
		this.limitState = limitState;
	}
	public String getLimitType() {
		return limitType;
	}
	@XmlElement(name = "limitType", required = false)
	public void setLimitType(String limitType) {
		this.limitType = limitType;
	}
	public String getKindCode() {
		return kindCode;
	}
	@XmlElement(name = "kindCode", required = false)
	public void setKindCode(String kindCode) {
		this.kindCode = kindCode;
	}
	public String getKindName() {
		return kindName;
	}
	@XmlElement(name = "kindName", required = false)
	public void setKindName(String kindName) {
		this.kindName = kindName;
	}
	public String getCurrency() {
		return currency;
	}
	@XmlElement(name = "currency", required = false)
	public void setCurrency(String currency) {
		this.currency = currency;
	}
	public String getValue() {
		return value;
	}
	@XmlElement(name = "value", required = false)
	public void setValue(String value) {
		this.value = value;
	}
	public String getIsPrint() {
		return isPrint;
	}
	@XmlElement(name = "isPrint", required = false)
	public void setIsPrint(String isPrint) {
		this.isPrint = isPrint;
	}
	public String getState() {
		return state;
	}
	@XmlElement(name = "state", required = false)
	public void setState(String state) {
		this.state = state;
	}
	public String getRemark() {
		return remark;
	}
	@XmlElement(name = "remark", required = false)
	public void setRemark(String remark) {
		this.remark = remark;
	}
}
