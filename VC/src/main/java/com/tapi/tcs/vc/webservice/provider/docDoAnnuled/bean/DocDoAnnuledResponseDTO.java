package com.tapi.tcs.vc.webservice.provider.docDoAnnuled.bean;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlElement;
import javax.xml.bind.annotation.XmlRootElement;
import javax.xml.bind.annotation.XmlType;

@XmlAccessorType(XmlAccessType.FIELD)
@XmlType(name = "DZZF_RESP_BODY", propOrder = {
		"doAnnuledStatus",
		"docVerCode",
		"docNum"
})
public class DocDoAnnuledResponseDTO {
	
	/** 单证作废成功与否标记 */
	@XmlElement(name = "doAnnuledStatus", required = true)
	private String doAnnuledStatus;
	/** 单证类型代码*/
	@XmlElement(name = "docVerCode", required = true)
	private String docVerCode;
	/**单证流水号 */
	@XmlElement(name = "docNum", required = true)
	private String docNum;
	public String getDoAnnuledStatus() {
		return doAnnuledStatus;
	}
	public void setDoAnnuledStatus(String doAnnuledStatus) {
		this.doAnnuledStatus = doAnnuledStatus;
	}
	public String getDocVerCode() {
		return docVerCode;
	}
	public void setDocVerCode(String docVerCode) {
		this.docVerCode = docVerCode;
	}
	public String getDocNum() {
		return docNum;
	}
	public void setDocNum(String docNum) {
		this.docNum = docNum;
	}
	
	
}
