package com.tapi.tcs.vc.webservice.provider.invoiceDoUsed.bean;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlElement;
import javax.xml.bind.annotation.XmlType;

@XmlAccessorType(XmlAccessType.FIELD)
@XmlType(name = "FPHX_RESP_RES", propOrder = {
    "status",
    "docVerCode",
    "docNum",
    "businessNo",
    "pressBatchNo",
    "remark"
})
public class InvoiceDoUsedResultDTO {
	@XmlElement(name = "status", required = true)
	private String status;//结果状态
	@XmlElement(name = "docVerCode", required = true)
	private String docVerCode;//单证类型代码
	@XmlElement(name = "docNum", required = true)
	private String docNum;//单证流水号
	@XmlElement(name = "businessNo", required = true)
	private String businessNo;//业务号
	@XmlElement(name = "pressBatchNo", required = true)
	private String pressBatchNo;//印刷批次
	@XmlElement(name = "remark", required = true)
	private String remark;
	
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getDocVerCode() {
		return docVerCode;
	}
	public void setDocVerCode(String docVerCode) {
		this.docVerCode = docVerCode;
	}
	public String getDocNum() {
		return docNum;
	}
	public void setDocNum(String docNum) {
		this.docNum = docNum;
	}
	public String getBusinessNo() {
		return businessNo;
	}
	public void setBusinessNo(String businessNo) {
		this.businessNo = businessNo;
	}
	public String getPressBatchNo() {
		return pressBatchNo;
	}
	public void setPressBatchNo(String pressBatchNo) {
		this.pressBatchNo = pressBatchNo;
	}
    public String getRemark() {
        return remark;
    }
    public void setRemark(String remark) {
        this.remark = remark;
    }
	
	
}
